export const getRecommendations = (occasion, style, budgetCategory) => {
  return products.filter(p => {
    const occasionMatch = !occasion || p.tags.includes(occasion.toLowerCase());
    const styleMatch = !style || p.tags.includes(style.toLowerCase());

    let budgetMatch = true;
    if (budgetCategory === 'low') budgetMatch = p.price < 1500;
    if (budgetCategory === 'medium') budgetMatch = p.price >= 1500 && p.price <= 4000;
    if (budgetCategory === 'high') budgetMatch = p.price > 4000;

    return budgetMatch && (occasionMatch || styleMatch);
  }).slice(0, 3);
};
